package jx.net.protocol.tcp;

class Flags {
  /* TCP Flags */
   static final int FIN  =   0x0001;
   static final int SYN  =   0x0002;
   static final int RST  =   0x0004;
   static final int PUSH =   0x0008;
   static final int ACK  =   0x0010;
   static final int URG  =   0x0020;
   static final int DO   =   0xF000;
}
